// context-generator.js — generates LLM-ready context markdown from .arc AST

'use strict'

const fs = require('fs')
const path = require('path')

class ContextGenerator {
  constructor(ast, cwd) {
    this.ast = ast
    this.cwd = cwd
  }

  outputDir() {
    const dotArcPath = path.join(this.cwd, '.arc')
    if (fs.existsSync(dotArcPath) && fs.statSync(dotArcPath).isDirectory()) {
      return dotArcPath
    }
    return path.join(this.cwd, '.arc-output')
  }

  outputFilePath() {
    return path.join(this.outputDir(), 'LLM_CONTEXT.md')
  }

  statusBadge(status) {
    if (status === 'built') return 'built'
    if (status === 'in-progress') return 'in-progress'
    if (status === 'planned') return 'planned'
    return status
  }

  resolveExample(pattern, name = 'notes') {
    const pascal = name.charAt(0).toUpperCase() + name.slice(1)
    return pattern
      .replace('{domain}', name.toLowerCase())
      .replace('{Name}', pascal)
      .replace('{name}', name)
      .replace('{path}', `${name}/example`)
  }

  generatePreamble() {
    const now = new Date().toISOString()
    return `# CODEBASE CONTEXT
> Generated by arc · ${now}

## Instructions for the AI Reading This File

You are working on a codebase with strict architectural rules enforced by \`arc check\`.
Read this entire file before writing any code. Then follow these rules exactly:

1. **Architecture is enforced.** Every rule in this file is checked by \`arc check\` before build.
2. **Use what exists.** Check the Features section before creating anything new.
3. **Follow layer rules.** Each layer has strict import boundaries and constraints.
4. **Follow naming conventions.** Use the conventions table when creating files.
5. **Directives are required.** Respect required directives (for example, \`'use server'\`).
6. **When in doubt, ask.** If layer placement is ambiguous, state reasoning first.
7. **Keep types centralized.** Prefer shared types from the types layer.
8. **Respect DB schema.** Do not invent tables or columns.

---
`
  }

  generateProject() {
    const { project } = this.ast
    if (!project) return '## Project\n\nNot declared.\n\n---\n'

    return `## Project

**Name:** ${project.name || project.id}
**Stack:** ${project.stack.join(' + ')}
**Language:** ${project.language}

---
`
  }

  generateLayers() {
    const { layers } = this.ast
    if (!layers.length) return '## Architecture\n\nNo layers declared.\n\n---\n'

    const rows = layers.map((l) => {
      const canImport = l.canImport.length ? l.canImport.join(', ') : 'none'
      const purpose = l.description || 'No description'
      return `| ${l.name} | \`${l.path}\` | ${canImport} | ${purpose} |`
    }).join('\n')

    const details = layers.map((l) => {
      const lines = [`- **${l.name}** (\`${l.path}\`): ${l.description || 'No description.'}`]
      if (l.directive) lines.push(`  Requires directive: \`${l.directive}\`.`)
      if (l.requireFilename) lines.push(`  Required filename pattern: \`${l.requireFilename}\`.`)
      if (l.requireExports.length) lines.push(`  Required export types: ${l.requireExports.map(v => `\`${v}\``).join(', ')}.`)
      if (l.allowExports.length) lines.push(`  Allowed export types: ${l.allowExports.map(v => `\`${v}\``).join(', ')}.`)
      if (l.forbidExports.length) lines.push(`  Forbidden export types: ${l.forbidExports.map(v => `\`${v}\``).join(', ')}.`)
      if (l.readonly) lines.push('  Readonly layer: do not modify files manually.')
      return lines.join('\n')
    }).join('\n\n')

    return `## Architecture — How This Codebase Is Structured

This codebase uses layered architecture. Each layer can only import from allowed layers.

| Layer | Path | Can Import From | Purpose |
|---|---|---|---|
${rows}

### Layer Rules

${details}

---
`
  }

  generateRules() {
    const { rules } = this.ast
    if (!rules.length) return '## Architecture Rules — What Is Forbidden\n\nNo custom rules declared.\n\n---\n'

    const chunks = rules.map((rule) => {
      const lines = []
      lines.push(`### ${rule.id} [${rule.severity}]`)
      lines.push(rule.description || 'No description')
      lines.push(`- **Forbidden:** ${rule.forbidType} \`${rule.forbidValue}\``)
      if (rule.exceptIn && rule.exceptIn.length) lines.push(`- **Allowed in:** ${rule.exceptIn.join(', ')}`)
      if (rule.inLayers && rule.inLayers.length) lines.push(`- **Checked in:** ${rule.inLayers.join(', ')}`)
      return lines.join('\n')
    }).join('\n\n')

    return `## Architecture Rules — What Is Forbidden

The following rules are enforced by \`arc check\`.

${chunks}

---
`
  }

  generateEnforcements() {
    const { enforcements } = this.ast
    const lines = []

    if (enforcements.noCircularImports) {
      lines.push('- **No circular imports** — Files must not form import cycles.')
    }
    if (enforcements.noDeadExports) {
      lines.push('- **No dead exports** — Exported symbols should be imported somewhere.')
    }
    if (enforcements.noImplicitAny) {
      lines.push('- **No implicit any** — Parameters should have explicit types.')
    }
    if (enforcements.noFloatingPromises) {
      lines.push('- **No floating promises** — Async work should be awaited or handled.')
    }

    if (!lines.length) {
      return '## Global Enforcements\n\nNo global enforcements declared.\n\n---\n'
    }

    return `## Global Enforcements

${lines.join('\n')}

---
`
  }

  generateConventions() {
    const { conventions } = this.ast
    if (!conventions.length) return '## Naming Conventions\n\nNo conventions declared.\n\n---\n'

    const rows = conventions.map((cv) => {
      const example = this.resolveExample(cv.pattern)
      return `| ${cv.name} | \`${cv.pattern}\` | \`${example}\` |`
    }).join('\n')

    return `## Naming Conventions

| Convention | Pattern | Example |
|---|---|---|
${rows}

---
`
  }

  generateFeatures() {
    const { features = [] } = this.ast
    if (!features.length) return '## What Already Exists — Features\n\nNo features declared.\n\n---\n'

    const blocks = features.map((f) => {
      const lines = []
      lines.push(`### ${f.name} — ${this.statusBadge(f.status)}`)
      lines.push(f.description || 'No description')
      if (f.layer) lines.push(`**Layer:** ${f.layer}`)
      if (f.files.length) lines.push(`**Files:** ${f.files.map(v => `\`${v}\``).join(', ')}`)
      if (f.actions.length) lines.push(`**Server Actions:** ${f.actions.map(v => `\`${v}\``).join(', ')}`)
      if (f.hooks.length) lines.push(`**Hooks:** ${f.hooks.map(v => `\`${v}\``).join(', ')}`)
      if (f.components.length) lines.push(`**Components:** ${f.components.map(v => `\`${v}\``).join(', ')}`)
      if (f.routes.length) lines.push(`**Routes:** ${f.routes.join(', ')}`)
      if (f.tables.length) lines.push(`**Tables:** ${f.tables.join(', ')}`)
      if (f.dependsOn.length) lines.push(`**Depends on:** ${f.dependsOn.join(', ')}`)
      if (f.notes) lines.push(`**Note:** ${f.notes}`)
      return lines.join('\n')
    }).join('\n\n')

    return `## What Already Exists — Features

This is your feature inventory. Extend built features; avoid rebuilding them.

${blocks}

---
`
  }

  generateFlows() {
    const { flows } = this.ast
    if (!flows.length) return '## Data Flows\n\nNo flows declared.\n\n---\n'

    const blocks = flows.map((f) => {
      const lines = []
      lines.push(`### ${f.name}`)
      lines.push(f.description || '')
      if (f.steps.length) lines.push(`\n${f.steps.map((step, i) => `${i + 1}. ${step}`).join('\n')}`)
      if (f.touches.length) lines.push(`\n**Touches:** ${f.touches.map(v => `\`${v}\``).join(', ')}`)
      if (f.tables.length) lines.push(`**Tables:** ${f.tables.join(', ')}`)
      if (f.fields.length) lines.push(`**Key fields:** ${f.fields.join(', ')}`)
      return lines.join('\n')
    }).join('\n\n')

    return `## Data Flows

${blocks}

---
`
  }

  generateHowTo() {
    return `## How To Work With This Codebase

### Adding a new feature

1. Pick the target layer(s).
2. Use \`arc scaffold <type> <name>\` where it applies.
3. Follow naming conventions and required directives.
4. Keep server logic in server layers and client logic in client layers.
5. Run \`arc check\` and fix violations before build.

### Layer you should put code in

| I'm writing... | Put it in |
|---|---|
| Database query | \`src/actions/\` or approved infra layer |
| Client-side state | \`src/hooks/\` |
| UI component | \`src/components/app/\` |
| Shared TypeScript type | \`src/types/\` |
| API endpoint | \`src/app/api/{path}/route.ts\` |
| Infrastructure client | \`src/lib/\` |

---
`
  }

  generateWhatNotToDo() {
    const hardRules = this.ast.rules.filter(r => r.severity === 'error')
    if (!hardRules.length) return ''

    const items = hardRules.map((r) => {
      const scope = r.exceptIn && r.exceptIn.length ? ` outside of ${r.exceptIn.join(', ')}` : ''
      return `- Do NOT ${r.forbidType} \`${r.forbidValue}\`${scope} — ${r.description}`
    }).join('\n')

    return `## What NOT to do

${items}

---
`
  }

  generateCriticalFiles() {
    const { context } = this.ast
    if (!context || !context.criticalFiles || context.criticalFiles.length === 0) {
      return '## Critical Files\n\nNo critical files declared.\n\n'
    }

    const lines = context.criticalFiles.map((file) => `- \`${file}\``).join('\n')
    return `## Critical Files

${lines}

`
  }

  generateDoNotTouch() {
    const { context } = this.ast
    if (!context || !context.doNotTouch || context.doNotTouch.length === 0) {
      return '## Do Not Touch\n\nNo do-not-touch paths declared.\n\n'
    }

    const lines = context.doNotTouch.map((file) => `- \`${file}\``).join('\n')
    return `## Do Not Touch

${lines}

`
  }

  generateSummary() {
    const { context } = this.ast
    if (!context || !context.summary) return '## Full Summary\n\nNo summary declared.\n'

    return `## Full Summary

${context.summary}
`
  }

  generate() {
    return [
      this.generatePreamble(),
      this.generateProject(),
      this.generateLayers(),
      this.generateRules(),
      this.generateEnforcements(),
      this.generateConventions(),
      this.generateFeatures(),
      this.generateFlows(),
      this.generateHowTo(),
      this.generateWhatNotToDo(),
      this.generateCriticalFiles(),
      this.generateDoNotTouch(),
      this.generateSummary(),
    ].join('\n')
  }

  write() {
    const outDir = this.outputDir()
    const outPath = this.outputFilePath()
    fs.mkdirSync(outDir, { recursive: true })

    const content = this.generate()
    fs.writeFileSync(outPath, content)

    return {
      outputFile: path.relative(this.cwd, outPath) || path.basename(outPath),
      sizeBytes: Buffer.byteLength(content, 'utf8'),
    }
  }
}

module.exports = { ContextGenerator }
